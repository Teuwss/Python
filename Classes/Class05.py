# Soma.
adicao = 10 + 10
print(f'Adição = {adicao}')

# Subtração.
subtracao = 10 - 5
print(f'Subtração = {subtracao}')

# Multiplicação.
multiplicacao = 10 * 10
print(f'Multiplicação = {multiplicacao}')

# Divisão.
divisao = 10 / 3
print(f'Divisão = {divisao}')

# Divisão inteira.
divisao_int = 10 // 3
print(f'Divisão inteira = {divisao_int}')

# Exponenciação
exponenciacao = 2 ** 10
print(f'Exponenciação = {exponenciacao}')

# Módulo.
modulo = 55 % 2
print(f'Resto = {modulo}')

# Precedência.
conta = (1 + 1) ** 2 * 3 // 4 + 5 - 6
print(f"Precedência = {conta}")