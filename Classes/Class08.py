# Verifica se 2 é maior que 1.
maior = 2 > 1

# Verifica se 2 é maior ou igual a 1.
maior_ou_igual = 2 >= 1

# Verifica se 1 é menor que 2.
menor = 1 < 2

# Verifica se 1 é menor ou igual a 2.
menor_ou_igual = 1 <= 2

# Verifica se 2 é igual a 2.
igual = 2 == 2

# Verifica se 1 é diferente de 2.
diferente = 1 != 2

# Exibe os resultados de todas as comparações.
print(maior, maior_ou_igual, menor, menor_ou_igual, igual, diferente)
