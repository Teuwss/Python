# Aspas simples:
print('Ol치, mundo!')

# Aspas duplas:
print("Ol치, mundo!")

# Escape:
print("Ol치, \'mundo\'!")

# Raw string:
print(r"Ol치, \'mundo\'!")

# Inteiro -> Int:
print(10)
print(-3)

# Ponto flutuante -> Float:
print(10.5)
print(-3.75)

# Boolean -> Bool:
print(10 > 5)
print(3 == 4)
print(7 <= 7)
print("a" != "a")